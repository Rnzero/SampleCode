//Prevents player from going off Screen
void Player::Update()
{
	int Right = x + Width; 
	int Bottom = y + Height;

	if (x <= 5)
		x = 5;                  // if player is moving over left boundary, reset x-position to 5      
	else if (Right >= Graphics::ScreenWidth - 1)
		x = (Graphics::ScreenWidth - 1) - Width; // if player is moving over right boundary, reset x-position to x-width    
	else
		x = x;


	if (y <= 75)
		y = 75;              // if player is moving over top boundary, reset y-position to 75     
	else if (Bottom >= Graphics::ScreenHeight - 1)
		y = (Graphics::ScreenHeight - 1) - Height; // if player is moving over bottom boundary, reset y - position to y-Height
	else
		y = y;

}


